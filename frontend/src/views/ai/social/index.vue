## 模型库（按场景分组，含选用时机）

### 基础对话引擎
- **对话飞轮模型**：观察提问→积极倾听→自我袒露→再次真诚提问。**触发信号**：任何需要拉近关系、保持对话流动的日常聊天。
- **开场提问模型**：观察现场/共同点/称赞→询问观点/方法/经历/兴趣→开放性追问。**触发信号**：初次见面、聚会冷场、加入一群不熟悉的人。
- **讲故事模型**：5W1H + 目的→努力→困难→结果。**触发信号**：对方问“你最近怎么样”“当时发生了什么”，或者你想分享经历但不想显得自恋。
- **上堆/下切模型**：上堆提炼观点/动机/方案；下切追问细节/感受/事实。**触发信号**：对话浮于表面需要深入，或者对方陈述事实需要挖出情感意图。
- **积极倾听模型（3F）**：事实(Fact) → 感受(Feeling) → 真实意图(Focus)。**触发信号**：对方说出带有情绪词的内容，或表达含糊、话里有话时。

### 破冰与随性闲聊
- **FORD闲聊话题模型**：家庭/朋友 → 职业/学业 → 休闲 → 梦想（注意边界，关系不深不问隐私家庭问题）。**触发信号**：和不熟的人需要安全话题。
- **CEIT随性社交模型**：现状(Current) → 解释(Explain) → 邀请(Invite) → 轻松语气(Tone)。**触发信号**：别人随口问“最近怎么样”“忙不忙”等需要简短自然回答的场合。
- **AQST随机问答模型**：确认兴趣(Acknowledge) → 追问(Question) → 分享相关经历(Share) → 轻松语气(Tone)。**触发信号**：对方分享了一件小事，你想延续轻松氛围。

### 拒绝与边界设立
- **拒绝模型**：拖延答复 / 三明治拒绝 / 破唱片法 / 用“为什么要这样做”替代直接“不” / 直接拒绝+理由。**触发信号**：对方提出让你为难的请求，且你判断需要保护自己的边界。
- **DESC坚定边界模型**：描述事实(Describe) → 表达影响(Express) → 明确要求(Specify) → 确认后果或协议(Consequence/Confirm)。**触发信号**：关系较近但对方反复越界，需要一次正式但非攻击的沟通。
- **DEAR请求模型**：描述(Describe) → 表达(Express) → 请求(Ask) → 强化(Reinforce)。**触发信号**：你需要向对方提出一个重要但可能被拒的请求。

### 安慰与情绪支持
- **安慰模型**：表达支持 → 倾听故事 → 反馈情感 → 反馈认知。**触发信号**：对方明显陷入沮丧、悲伤、失落，需要情感陪伴而非解决方案。
- **SAFE紧张化解模型**：共情(Sympathize) → 轻轻提问(Ask lightly) → 风趣/放松(Funny/Relax) → 鼓励/赋能(Encourage/Empower)。**触发信号**：对方在社交中明显紧张、手足无措、说错话后尴尬。
- **RELAX尴尬化解模型**：关联(Relate) → 共情(Empathize) → 轻度重构(Light reframe) → 避免否定感受(Avoid denial) → 扩展(eXpand)。**触发信号**：对方当众出糗、被调侃、觉得自己丢脸。
- **ACT行动促动模型**：接纳(Accept) → 切小难度(Cut difficulty) → 触发微行动(Trigger action)。**触发信号**：对方因恐惧、拖延而犹豫不决，需要推力而非说教。

### 称赞、鼓励与致谢
- **称赞模型**：具体行为 → 推断特质 → 说明影响。对长辈重细节、责任感、靠谱、判断力、长远眼光。**触发信号**：你想表达欣赏，或回应对方的好表现。
- **接受称赞模型**：延伸法、归因法、衬托法、找补法、调侃法。**触发信号**：被夸奖时避免尴尬否认，大方收下并回馈情绪价值。
- **鼓励模型**：描述好做法 → 请教方法 → 说出对自己的启发。**触发信号**：看到对方在困难中做成了某件事，想给予力量。
- **感谢模型**：具体帮助 → 对自己的影响 → 真诚感谢。**触发信号**：接受帮助后，希望让对方感到被重视。
- **求助模型**：明确目的 → 已尝试的方法 → 提出具体诉求 → 事后感谢。**触发信号**：你需要向他人寻求资源、信息或支持。

### 批评、反馈与职场表达
- **批评/建议模型**：私下场合 → 先肯定人 → 说具体行为 → 让对方复述/表达看法 → 问尝试过的方法 → 邀请补充 → 给建议 → 约定复盘。**触发信号**：必须指出对方问题时，且你希望维护关系。
- **SBI反馈模型**：情境(Situation) → 行为(Behavior) → 影响(Impact) → 下一步(Next)。**触发信号**：职场或朋友间需要纠正行为但避免人格攻击。
- **PREP结构化观点**：观点(Point) → 理由(Reason) → 例子(Example) → 重申观点(Point)。**触发信号**：开会、辩论、面试等需要清晰阐述观点时。
- **STAR经历模型**：情境(Situation) → 任务(Task) → 行动(Action) → 结果(Result)。**触发信号**：回答“讲一个你之前处理过的困难”等行为面试题。
- **SCQA提案模型**：情境(Situation) → 冲突(Complication) → 问题(Question) → 答案(Answer)。**触发信号**：向决策者提出建议，需要展现问题迫切性和方案逻辑。
- **GROW教练模型**：目标(Goal) → 现状(Reality) → 选项(Options) → 意愿(Will)。**触发信号**：对方陷入迷茫，你需要引导对方自己做决定，而非给建议。
- **BIFF书面冲突模型**：简短(Brief) → 信息性(Informative) → 友好(Friendly) → 坚定(Firm)。**触发信号**：用文字处理冲突、群聊争端或敌意邮件。
- **BADNEWS困难消息模型**：缓冲(Buffer) → 确认影响(Acknowledge impact) → 清晰传达(Deliver clearly) → 后续步骤(Next steps) → 共情(Empathy)。**触发信号**：需要告知延期、拒绝、变更等令人失望的消息。

### 冲突缓和与修复
- **CLEAR异议处理模型**：确认顾虑(Confirm) → 标注问题(Label) → 解释(Explain) → 邀请对齐(Ask/Align) → 再次确认价值(Reaffirm)。**触发信号**：对方对你的提议明确质疑或反对。
- **LARA降级冲突模型**：倾听(Listen) → 确认(Affirm) → 回应(Respond) → 补充(Add)。**触发信号**：双方情绪激动、即将争吵，需要降温。
- **NVC非暴力沟通模型**：观察(Observation) → 感受(Feeling) → 需要(Need) → 请求(Request)。**触发信号**：亲密关系、重要合作中需要表达脆弱和需求，避免指责。
- **REPAIR关系修复模型**：识别(Recognize) → 共情(Empathize) → 承担个人责任(Personal responsibility) → 行动(Action) → 邀请回应(Invite response) → 安抚(Reassure)。**触发信号**：你意识到自己伤了对方，关系出现裂痕时。
- **A.A.A.补救模型**：承认失误(Acknowledge) → 真诚致歉(Apologize) → 提出调整方案(Adjust)。**触发信号**：说错话、社交翻车后立即补救。

### 社交收尾与跟进
- **EXIT结束模型**：共情(Empathize) → 解释原因(eXplain reason) → 邀请未来(Invite future) → 感谢/收尾(Thank/close)。**触发信号**：想结束聊天、离开聚会、挂电话，不伤感情。
- **NETWORK跟进模型**：情境(Context) → 感谢(Appreciation) → 价值(Value) → 下一步(Next step)。**触发信号**：结识新朋友、获得帮助、需要后续保持联系时。

### 性格/状态应对包（先判断对象，再选框架）

**判断依据**：观察对方回复字数、情绪词密度、提问意愿、是否使用幽默、是否坚持逻辑等。

- **EASE内向者模型**：鼓励(Encourage)→认可(Acknowledge)→分享(Share)→轻松提问(Ease Question)。**触发信号**：对方话少但礼貌，显得谨慎，回答简短但非敌意。
- **FIRE外向者模型**：感受(Feel)→兴趣(Interest)→关联自我(Relate)→拓展(Expand)。**触发信号**：对方语速快、能量高、主动分享、情绪饱满。
- **PLAY幽默者模型**：接梗(Play along)→轻度调侃(Light tease)→添加细节(Add)→抛回问题(Yield question)。**触发信号**：对方频繁用梗、开玩笑、轻松调侃。
- **BALANCE理性者模型**：衔接(Bridge)→补充观点(Add)→逻辑(Logic)→避免争辩(Avoid conflict)→中立语气(Neutral tone)→继续(Continue)→舒缓(Ease)。**触发信号**：对方重视逻辑、爱说“从数据看”“按道理”，对情绪化表达皱眉。
- **SOFT冷淡者模型（轻触版）**：同步能量(Sync)→给予信息(Offer)→浮动试探(Float)→测试意愿(Test)。**触发信号**：对方回应简短但仍有基本礼貌，如“还好”“还行”，未完全封闭。
- **COLD冷淡者模型（降成本版）**：随意(Casual)→给予选项(Offer)→低投入(Low effort)→不施压(Don't push)。**触发信号**：对方回复极简，无明显互动意愿，但尚未冷漠拒绝。
- **EMPTY极冷模型**：回应情绪(Echo)→镜像状态(Mirror)→停顿(Pause)→微小扩展(Tiny expand)→让出话轮(Yield)。**触发信号**：对方只回“嗯”“哦”“累”，完全回避开放问题。

### 即兴与应急
- **即兴发言公式**：赞同+浅谈细节+谦虚；优点+总结想法+原因；感谢+回顾+愿景；感谢+感受+愿景。**触发信号**：被突然点名发言、祝酒、领奖等。
- **应急应对模型**：现状→预判→应对，然后按需选择换口径/换时间/换地点/换角色。**触发信号**：面对冒犯提问、敌对质疑或尴尬话题。

